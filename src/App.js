function normalizeName(name) {
  if (!name) return name
  const ru = EN_TO_RU[name] || name
  // Check direct match first
  if (LEGACY_NAMES[ru]) return LEGACY_NAMES[ru]
  // Strip grip variant e.g. "Тяга вниз (Узкий)" → "Тяга вниз" then remap
  const base = ru.replace(/\s*\([^)]*\)\s*$/, '').trim()
  if (LEGACY_NAMES[base]) {
    const grip = ru.match(/\(([^)]*)\)/)?.[1]
    const newBase = LEGACY_NAMES[base]
    return grip ? `${newBase} (${grip})` : newBase
  }
  return ru
}

function getExImage(name) {
  if (!name) return null
  const norm = normalizeName(name)
  if (EXERCISE_IMAGES[norm]) return EXERCISE_IMAGES[norm]
  if (EXERCISE_IMAGES[name]) return EXERCISE_IMAGES[name]
  // Strip grip variant e.g. "Тяга вертикального блока (Узкий)" → "Тяга вертикального блока"
  const base = norm.replace(/\s*\([^)]*\)\s*$/, '').trim()
  if (EXERCISE_IMAGES[base]) return EXERCISE_IMAGES[base]
  return null
}

function formatMonth(m) {
  if (!m) return ''
  const [y,mo] = m.split('-')
  const s = new Date(parseInt(y), parseInt(mo)-1).toLocaleDateString('ru', {month:'long', year:'numeric'})
  return s.charAt(0).toUpperCase() + s.slice(1)
}

function formatDateShort(d) {
  return new Date(d).toLocaleDateString('ru', { day:'numeric', month:'long' })
}

